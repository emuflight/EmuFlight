static int8_t bmi270ProcessGyroCas(uint8_t raw)
{
    int8_t result = 0;
    raw = raw & BMI270_GYRO_CAS_MASK;
    if ((raw & BMI270_GYRO_CAS_SIGN_BIT_MASK) == 0) {
        result = (int8_t)(raw);
    } else {
        result = (int8_t)(raw - 128);
    }
    return result;
}

